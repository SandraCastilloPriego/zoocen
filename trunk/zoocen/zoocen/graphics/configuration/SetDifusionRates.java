    public void setValues() {
        try {
            if (this.Neighbours != null) {
                int IDCellNeighbourg = (Integer) this.Neighbours.elementAt(this.jComboBoxCell.getSelectedIndex());
                for (int i = 0; i < this.World.cells.elementAt(this.CellID).getNSpecies(); i++) {
                    if (this.jComboBoxSpecie != null && this.World.cells.elementAt(this.CellID).getName(i) != null && this.World.cells.elementAt(this.CellID).getName(i).compareTo((String) this.jComboBoxSpecie.getSelectedItem()) == 0) {
                        double difusion = this.World.cells.elementAt(this.CellID).getDifusionRate(IDCellNeighbourg, i);
                        this.jSliderDifusion.setValue((int) (difusion * 100));
                    }
                }
            }
        } catch (Exception exception) {
        }
    }

