    private void setDifusion() {
        try {
            double Difusion = ((double) this.jSliderDifusion.getValue()) / 100;
            int IDCellNeighbourg = (Integer) this.Neighbours.elementAt(this.jComboBoxCell.getSelectedIndex());
            for (int i = 0; i < this.World.cells.elementAt(this.CellID).getNSpecies(); i++) {
                if (this.World.cells.elementAt(this.CellID).getName(i).compareTo((String) this.jComboBoxSpecie.getSelectedItem()) == 0) {
                    this.World.cells.elementAt(this.CellID).setDifusionRate(IDCellNeighbourg, (String) this.jComboBoxSpecie.getSelectedItem(), Difusion);
                }
            }
        } catch (Exception e) {
        }
    }

    public void setCellCombobox() {
        for (int i = 0; i < this.Neighbours.size(); i++) {
            int num = (Integer) this.Neighbours.elementAt(i) + 1;
            this.jComboBoxCell.addItem("Cell " + num);
        }
        System.out.println("setCellCombobox : " + this.CellID);
        for (int i = 0; i < this.World.cells.size(); i++) {
            Cell cell = this.World.cells.elementAt(i);
            if (cell.cellID == this.CellID) {
                for (int e = 0; e < cell.getNSpecies(); e++) {
                    this.jComboBoxSpecie.addItem(cell.getName(e));
                }
            }
        }
    }
